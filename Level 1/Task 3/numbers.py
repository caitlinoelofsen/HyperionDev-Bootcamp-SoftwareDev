# Used input function to obtain three different numbers from user
integer_1 = int(input("Input a whole number. "))
integer_2 = int(input("Input a second whole number. "))
integer_3 = int(input("Input a third whole number. "))
# Converted strings to integers and used addition operator to print the sum of all the numbers
print(integer_1 + integer_2 + integer_3)
# Used subtraction operator to print the difference between first and second number
print(integer_1 - integer_2)
# Used multiplication operator
print(integer_3 * integer_1)
# Input the addition sum in parenthesis and divided the sum by the third number
print((integer_1 + integer_2 + integer_3) / integer_3)